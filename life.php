                    <div class="left">
                        <h3>LIFE INSURANCE</h3>
                        <p class="details">
                          A contract between an individual and an insurer where the insurer promises to pay 
                          the individual's listed beneficiaries a sum of money upon the death of the insured person.
                        </p>
                    </div>
                    <div class="left2">
                        <h3>PREMIUM</h3>
                            <p class="details">
                                The amount an insurance provider charges an individual to manage the risks related to the policy.
                            </p>
                    </div>
                    <div class="left3">
                        <h3>DEATH BENEFIT</h3>
                            <p class="details">
                                The amount a beneficiary receives when an insured individual dies.
                            </p>
                    </div>

                    <div class="left4">
                        <h3>CLAIMS</h3>
                            <p class="details">
                                The beneficiary (ies) files a death claim with the insurance company,&nbsp; submitting a certified copy 
                                of the death certificate. Many companies allow insurers 30 days to review the claim. Then they can pay 
                                it,&nbsp; deny it or ask for additional information. 
                            </p>
                    </div>

                    <div class="left5">
                        <h3>MATURITY DATE</h3>
                            <p class="details">
                                A life insurance policy reaches maturity upon the death of the policyholder or when the period stated 
                                in the contract expires.
                            </p>
                    </div>

                    <div class="left6">
                        <h3>BENEFICIARY</h3>
                            <p class="details">
                                An individual who is entitled to receive the benefits of an insurance policy. The policyholder declares
                                his or her beneficiaries upon signing the insurance contract.
                            </p>
                    </div>

                    <div class="left7">
                        <h3>RIDER BENEFITS</h3>
                            <p class="details">
                                A rider is an add-on provision to a basic insurance policy that provides additional benefits to the
                                policyholder at an additional cost.
                            </p>
                    </div>

                    <div class="left8">
                    <h3>RISKS</h3>
                        <p class="details">
                                Insurance providers project the risks that may occur to a policyholder. The providers usually calculate 
                                the risks based on factors such as age and medical history. Higher risk entails higher and more expensive coverage.
                        </p>
                    </div>

                    <div class="left9">
                        <h3>ENDOWMENT</h3>
                            <p class="details">
                                A type of insurance that provides benefits both when death occurs during the term of the policy and when the 
                                insured individual is alive at the end of such a term 
                            </p>
                    </div>

